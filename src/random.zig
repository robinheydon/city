const std = @import("std");
const root = @import("root");

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub fn init() void {
    root.state.isaac64 = std.rand.Isaac64.init(0);
    root.state.random = root.state.isaac64.random();
}

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

pub fn rand(comptime T: type) T {
    switch (T) {
        f32 => return root.state.random.float(T),
        f64 => return root.state.random.float(T),
        bool => return root.state.random.boolean(),
        else => return root.state.random.int(T),
    }
}

